function buildCards(words, level) {
  return words.map((w, i) => {
    const idx = String(i + 1).padStart(4, '0');
    return {
      id: `hsk${level}_${idx}`,
      hanzi: w[0],
      pinyin: w[1],
      english: w[2],
      partOfSpeech: w[3] || '',
      exampleZh: w[4] || '',
      examplePinyin: w[5] || '',
      exampleEn: w[6] || '',
      hskLevel: level,
    };
  });
}

const allCards = [
  ...buildCards(hsk1, 1),
  ...buildCards(hsk2, 2),
  ...buildCards(hsk3, 3),
  ...buildCards(hsk4, 4),
  ...buildCards(hsk5, 5),
  ...buildCards(hsk6, 6),
];

const output = {
  version: '2.0',
  source: 'HSK Standard Vocabulary',
  totalCards: allCards.length,
  cards: allCards,
};

import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';
const __dirname = dirname(fileURLToPath(import.meta.url));
const outPath = resolve(__dirname, '../src/data/hsk.json');

writeFileSync(
  outPath,
  JSON.stringify(output, null, 2),
  'utf-8'
);

